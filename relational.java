import java.util.*;
public class relational{
    public static void main(String args[]){
        int a=8,b=5;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
    }
}